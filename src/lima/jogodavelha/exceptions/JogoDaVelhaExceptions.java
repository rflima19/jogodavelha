package lima.jogodavelha.exceptions;

public class JogoDaVelhaExceptions extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -331689982076551894L;

	public JogoDaVelhaExceptions() {
		super();
	}

	public JogoDaVelhaExceptions(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public JogoDaVelhaExceptions(String message, Throwable cause) {
		super(message, cause);
	}

	public JogoDaVelhaExceptions(String message) {
		super(message);
	}

	public JogoDaVelhaExceptions(Throwable cause) {
		super(cause);
	}
}
